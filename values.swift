
// Strings can be added with +
println("swift" + "lang")                  // swiftlang

println(1 + 1)                             // 2

// Casting an Int to a String
println("1+1 = " + String(1 + 1))          // 1+1 = 2

println(7.0 / 3.0)                         // 2.33333333333333

println(String(7.0) + " / " + String(3.0)) // 7.0 / 3.0

// Booleans are booleans (YES)
println(true && false)                     // false
println(true || false)                     // true
println(!true)                             // false
